# ── Constraint roles ─────────────────────────────────────────────────────
# OpenFOAM REQUIRES patchField type to match polyMesh patch type for
# constraint patches.  ``type symmetry;`` on a symmetry patch is the only
# legal block — any other type triggers "inconsistent patch and patchField
# types" and aborts the solver at startup.
#
# patch_role() returns one of these strings when the patch name suffix
# (``_symmetry`` / ``_top``/_bottom/_front/_back) or the mesh-side type
# matches a constraint.  Every per-field BC picker below MUST short-circuit
# to a bare ``{"type": <constraint>}`` when this fires — no ``value``, no
# ``inletValue``, no other entries.

_CONSTRAINT_ROLE_TO_TYPE: dict[str, str] = {
    "symmetry":      "symmetry",
    "symmetryplane": "symmetryPlane",
    "empty":         "empty",
    "wedge":         "wedge",
}


def _constraint_bc(role: str) -> dict[str, str] | None:
    """Return ``{"type": <constraint>}`` if ``role`` is a constraint role.

    Constraint patchField types take no other entries — caller short-circuits
    field-specific logic when this returns a dict.  Returns ``None`` for
    regular roles (inlet/outlet/wall) so the caller's normal logic runs.
    """
    t = _CONSTRAINT_ROLE_TO_TYPE.get(role)
    return {"type": t} if t else None


def _u_bc(role: str, region: "RegionSpec", spec: dict[str, Any]) -> dict[str, str]:
    """Velocity BC body for one patch.

    Inlet uses the user-supplied vector when ``spec.value`` is a 3-list,
    otherwise the region's ``U_init``.  Outlets release flow with
    ``inletOutlet``; walls (and coupled CHT interfaces) are ``noSlip``.
    Constraint patches (symmetry / empty / wedge) short-circuit to the
    matching constraint patchField type — required by OpenFOAM, no other
    entries allowed.
    """
    if (c := _constraint_bc(role)) is not None:
        return c
    if role == "inlet":
        val = spec.get("value")
        if isinstance(val, (list, tuple)) and len(val) == 3:
            u = (float(val[0]), float(val[1]), float(val[2]))
        elif isinstance(val, (int, float)):
            # Scalar velocity given (treat as magnitude in +x)
            u = (float(val), 0.0, 0.0)
        else:
            u = region.U_init
        return {"type": "fixedValue", "value": f"uniform {_u_to_str(u)}"}
    if role == "outlet":
        return {
            "type": "inletOutlet",
            "inletValue": "uniform (0 0 0)",
            "value": "uniform (0 0 0)",
        }
    # wall / symmetry / empty / wedge / default → noSlip (constraint patches
    # are rewritten by the universal BC fixer post-pass).
    return {"type": "noSlip"}


def _p_bc(role: str, region: "RegionSpec", spec: dict[str, Any]) -> dict[str, str]:
    """Static pressure ``p`` BC body — used by the ``calculated`` field for the
    chtMultiRegion solver pair; ``p_rgh`` is the actually-solved field."""
    if (c := _constraint_bc(role)) is not None:
        return c
    if role == "outlet":
        val = spec.get("value")
        p = float(val) if isinstance(val, (int, float)) else region.p_init
        return {"type": "fixedValue", "value": f"uniform {p:g}"}
    return {"type": "calculated", "value": f"uniform {region.p_init:g}"}


def _p_rgh_bc(role: str, region: "RegionSpec", spec: dict[str, Any]) -> dict[str, str]:
    """Modified pressure ``p_rgh`` BC body — solved field for buoyant CHT."""
    if (c := _constraint_bc(role)) is not None:
        return c
    if role == "outlet":
        val = spec.get("value")
        p = float(val) if isinstance(val, (int, float)) else region.p_init
        return {"type": "fixedValue", "value": f"uniform {p:g}"}
    return {"type": "fixedFluxPressure", "value": f"uniform {region.p_init:g}"}


def _T_bc_fluid(role: str, region: "RegionSpec", spec: dict[str, Any]) -> dict[str, str]:
    """Temperature BC body for a fluid region.  Coupled CHT interfaces use a
    dedicated block (see :func:`_coupled_T_block`); this picker handles the
    inlet / outlet / wall cases."""
    if (c := _constraint_bc(role)) is not None:
        return c
    if role == "inlet":
        val = spec.get("value")
        t = float(val) if isinstance(val, (int, float)) else region.T_init
        return {"type": "fixedValue", "value": f"uniform {t:g}"}
    if role == "outlet":
        return {
            "type": "inletOutlet",
            "inletValue": f"uniform {region.T_init:g}",
            "value": f"uniform {region.T_init:g}",
        }
    if role == "wall":
        val = spec.get("value")
        if isinstance(val, (int, float)):
            return {"type": "fixedValue", "value": f"uniform {float(val):g}"}
    return {"type": "zeroGradient"}


def _T_bc_solid(role: str, region: "RegionSpec", spec: dict[str, Any]) -> dict[str, str]:
    """Temperature BC body for a solid region — solids have no inlet/outlet so
    every non-coupled patch defaults to adiabatic (``zeroGradient``) unless the
    user explicitly fixed the temperature."""
    if (c := _constraint_bc(role)) is not None:
        return c
    val = spec.get("value")
    if isinstance(val, (int, float)):
        return {"type": "fixedValue", "value": f"uniform {float(val):g}"}
    return {"type": "zeroGradient"}


def _k_bc(role: str, region: "RegionSpec", spec: dict[str, Any]) -> dict[str, str]:
    """Turbulent kinetic energy BC body."""
    if (c := _constraint_bc(role)) is not None:
        return c
    val = spec.get("value")
    k = float(val) if isinstance(val, (int, float)) else region.k_init
    if role == "inlet":
        return {"type": "fixedValue", "value": f"uniform {k:g}"}
    if role == "outlet":
        return {
            "type": "inletOutlet",
            "inletValue": f"uniform {k:g}",
            "value": f"uniform {k:g}",
        }
    return {"type": "kqRWallFunction", "value": f"uniform {k:g}"}


def _epsilon_bc(role: str, region: "RegionSpec", spec: dict[str, Any]) -> dict[str, str]:
    if (c := _constraint_bc(role)) is not None:
        return c
    val = spec.get("value")
    e = float(val) if isinstance(val, (int, float)) else region.epsilon_init
    if role == "inlet":
        return {"type": "fixedValue", "value": f"uniform {e:g}"}
    if role == "outlet":
        return {
            "type": "inletOutlet",
            "inletValue": f"uniform {e:g}",
            "value": f"uniform {e:g}",
        }
    return {"type": "epsilonWallFunction", "value": f"uniform {e:g}"}


def _omega_bc(role: str, region: "RegionSpec", spec: dict[str, Any]) -> dict[str, str]:
    """Specific dissipation rate ω — wall function on walls, fixedValue at inlets.

    Pairs with k for kOmega / kOmegaSST.  Uses ``omegaWallFunction``
    (the canonical wall function for ω, same family as
    ``epsilonWallFunction`` / ``kqRWallFunction``).
    """
    if (c := _constraint_bc(role)) is not None:
        return c
    val = spec.get("value")
    w = float(val) if isinstance(val, (int, float)) else region.omega_init
    if role == "inlet":
        return {"type": "fixedValue", "value": f"uniform {w:g}"}
    if role == "outlet":
        return {
            "type": "inletOutlet",
            "inletValue": f"uniform {w:g}",
            "value": f"uniform {w:g}",
        }
    return {"type": "omegaWallFunction", "value": f"uniform {w:g}"}


def _nut_bc(role: str, region: "RegionSpec", spec: dict[str, Any]) -> dict[str, str]:
    """Turbulent viscosity — wall functions on walls; calculated elsewhere."""
    if (c := _constraint_bc(role)) is not None:
        return c
    if role == "wall":
        return {"type": "nutkWallFunction", "value": "uniform 0"}
    return {"type": "calculated", "value": "uniform 0"}


def _alphat_bc(role: str, region: "RegionSpec", spec: dict[str, Any]) -> dict[str, str]:
    """Turbulent thermal diffusivity (compressible) — wall function on walls.

    Pairs with :func:`_nut_bc` — same role-based wall-function/calculated
    split, but the compressible variants needed by the energy equation.
    The CHT base solver registers ``alphat`` on every fluid region's
    objectRegistry; without the file the case fails at startup with
    ``cannot find file 0/<fluid>/alphat``.
    """
    if (c := _constraint_bc(role)) is not None:
        return c
    if role == "wall":
        return {"type": "compressible::alphatWallFunction", "value": "uniform 0"}
    return {"type": "calculated", "value": "uniform 0"}



def region_patches(region_name: str, config: dict[str, Any]) -> list[str]:
    """Mesh patch names that belong to a region after ``splitMeshRegions``.

    Ownership rule: ``name.startswith(f"{region_name}_")`` — that captures
    every patch the user authored against this region (e.g.
    ``innerFluid_inlet``, ``innerFluid_outlet``, ``innerFluid_symmetry``).

    Patches whose mesh type is ``empty`` / ``wedge`` / ``symmetry`` and
    whose name has no region prefix (``front`` / ``back``) are shared
    across every region by OpenFOAM and are included in the result so
    the renderer emits an explicit block — the universal constraint-BC
    fixer in :mod:`bc_fixers` later forces the right ``type`` on them.

    Coupled interface patches (``<region>_to_<other>``) are *not* in the
    user's mesh patch list — they're auto-generated by
    ``splitMeshRegions``.  Callers add them separately via
    :attr:`RegionSpec.interfaces`.
    """
    mesh_patches = (config.get("mesh") or {}).get("patches") or []
    owned: list[str] = []
    shared: list[str] = []
    prefix = f"{region_name}_"
    for p in mesh_patches:
        name = p["name"] if isinstance(p, dict) else getattr(p, "name", None)
        ptype = (
            p.get("type") if isinstance(p, dict) else getattr(p, "type", "")
        ) or "patch"
        if not name:
            continue
        if name.startswith(prefix):
            owned.append(name)
        elif ptype.lower() in ("empty", "wedge", "symmetry", "symmetryplane"):
            shared.append(name)
    return owned + shared


_SUFFIX_ROLES: tuple[tuple[str, str], ...] = (
    ("_inlet",     "inlet"),
    ("_outlet",    "outlet"),
    ("_symmetry",  "symmetry"),
    ("_top",       "wall"),
    ("_bottom",    "wall"),
    ("_left",      "wall"),
    ("_right",     "wall"),
    ("_left_end",  "wall"),
    ("_right_end", "wall"),
    ("_wall",      "wall"),
    ("_walls",     "wall"),
)


def patch_role(patch_name: str, config: dict[str, Any]) -> str:
    """Infer one patch's role (inlet / outlet / wall / symmetry / empty / wedge).

    Authority order:
      1. ``config["boundary_conditions"][name]["patchClass"]`` (or
         ``patch_class`` / ``patch_type`` aliases).
      2. mesh patch type — ``symmetry`` / ``empty`` / ``wedge`` map verbatim.
      3. name suffix heuristic (``*_inlet`` → inlet, …).
      4. ``front`` / ``back`` → empty.
      5. default → ``wall``.
    """
    spec = (config.get("boundary_conditions") or {}).get(patch_name)
    if isinstance(spec, dict):
        pc = (
            spec.get("patchClass")
            or spec.get("patch_class")
            or spec.get("patch_type")
        )
        if isinstance(pc, str) and pc:
            return pc.lower()

    mesh_patches = (config.get("mesh") or {}).get("patches") or []
    for p in mesh_patches:
        n = p["name"] if isinstance(p, dict) else getattr(p, "name", None)
        if n != patch_name:
            continue
        t = (
            p.get("type") if isinstance(p, dict) else getattr(p, "type", "")
        ) or ""
        lo = t.lower()
        if lo in ("symmetry", "symmetryplane"):
            return "symmetry"
        if lo == "empty":
            return "empty"
        if lo == "wedge":
            return "wedge"
        break

    low = patch_name.lower()
    for suffix, role in _SUFFIX_ROLES:
        if low.endswith(suffix):
            return role
    if patch_name in ("front", "back"):
        return "empty"
    return "wall"


def patch_field_spec(
    patch_name: str, field: str, config: dict[str, Any],
) -> dict[str, Any]:
    """Return the precheck/UI-authored BC spec for one (patch, field).

    Always returns a dict — empty when nothing is recorded.  Keeps the
    callers ``.get(...)``-friendly so missing entries fall back to the
    role-based default without special-casing.
    """
    spec = (config.get("boundary_conditions") or {}).get(patch_name) or {}
    if not isinstance(spec, dict):
        return {}
    fs = spec.get(field)
    return fs if isinstance(fs, dict) else {}



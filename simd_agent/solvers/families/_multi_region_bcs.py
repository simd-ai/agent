def _block(name: str, body: dict[str, str]) -> str:
    """Format one ``<name> { … }`` patch BC block."""
    lines = [f"    {name}", "    {"]
    for key, value in body.items():
        lines.append(f"        {key:<14}  {value};")
    lines.append("    }\n")
    return "\n".join(lines)


def _wrap_field_file(
    region_name: str,
    object_name: str,
    field_class: str,
    dimensions: str,
    internal_field: str,
    boundary_blocks: str,
) -> str:
    """Wrap the boundaryField body with the standard FOAM header / footer."""
    return (
        "FoamFile\n{\n"
        "    version     2.0;\n"
        "    format      ascii;\n"
        f"    class       {field_class};\n"
        f"    location    \"0/{region_name}\";\n"
        f"    object      {object_name};\n"
        "}\n"
        "// * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * //\n\n"
        f"dimensions      {dimensions};\n"
        f"internalField   uniform {internal_field};\n\n"
        "boundaryField\n{\n"
        f"{boundary_blocks}"
        "}\n\n"
        "// ************************************************************************* //\n"
    )


def _u_to_str(u: tuple[float, float, float] | list[float]) -> str:
    return f"({u[0]:g} {u[1]:g} {u[2]:g})"



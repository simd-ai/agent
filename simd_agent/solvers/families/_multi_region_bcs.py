# Coupled CHT interface blocks
# ────────────────────────────────────────────────────────────────────────────


def _coupled_T_block(region: "RegionSpec", nbr: str) -> str:
    """Coupled temperature BC at a fluid-solid interface — the heart of CHT.

    OpenFOAM's ``compressible::turbulentTemperatureCoupledBaffleMixed``
    enforces both temperature continuity and Fourier heat-flux continuity
    across the interface.  Fluid sides use ``kappaMethod fluidThermo``
    so the BC reads κ from the local thermophysical model;
    solid sides use ``kappaMethod solidThermo``.
    """
    kappa = "fluidThermo" if region.kind == "fluid" else "solidThermo"
    return _block(
        f"{region.name}_to_{nbr}",
        {
            "type": "compressible::turbulentTemperatureCoupledBaffleMixed",
            "Tnbr": "T",
            "kappaMethod": kappa,
            "value": f"uniform {region.T_init:g}",
        },
    )


def _coupled_wall_block(
    region_name: str, nbr: str, body: dict[str, str],
) -> str:
    """Build a coupled-interface block for non-T fields.

    Velocity / pressure / turbulence don't cross the interface — it
    behaves like a wall from each side.  Same name convention as the
    coupled T block: ``<self>_to_<nbr>``.
    """
    return _block(f"{region_name}_to_{nbr}", body)



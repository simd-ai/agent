# Per-field file builders — the public API of this module
# ────────────────────────────────────────────────────────────────────────────


def build_region_0_U(region: "RegionSpec", config: dict[str, Any]) -> str:
    """``0/<region>/U`` with per-patch BCs.

    Fluid regions use the ``_u_bc`` picker (inlet → fixedValue, outlet →
    inletOutlet, walls → noSlip).  Solid regions don't solve momentum but
    ESI v2406's chtMultiRegionSimpleFoam still walks the solid mesh's
    objectRegistry for a ``U`` field at startup — emit the file with
    ``noSlip`` on every non-constraint patch (no flow in a solid; matches
    the OpenFOAM-4.x ``multiRegionHeaterRadiation`` ``0/U`` template
    distributed to its solid regions via changeDictionaryDict).
    Constraint patches still short-circuit to ``symmetry``/``empty``/``wedge``
    via :func:`_constraint_bc`.
    """
    blocks: list[str] = []
    for name in region_patches(region.name, config):
        role = patch_role(name, config)
        spec = patch_field_spec(name, "U", config)
        if region.kind == "solid":
            body = _constraint_bc(role) or {"type": "noSlip"}
        else:
            body = _u_bc(role, region, spec)
        blocks.append(_block(name, body))
    for nbr in region.interfaces or ():
        blocks.append(_coupled_wall_block(region.name, nbr, {"type": "noSlip"}))
    return _wrap_field_file(
        region.name, "U", "volVectorField", "[0 1 -1 0 0 0 0]",
        _u_to_str(region.U_init), "".join(blocks),
    )


def build_region_0_p(region: "RegionSpec", config: dict[str, Any]) -> str:
    """``0/<region>/p`` — calculated everywhere except fluid outlets.

    Solid regions: every patch (and every coupled interface) is
    ``calculated`` — pressure is a passive field in the solid, the
    solidThermo only reads it.  Fluid regions: the existing :func:`_p_bc`
    picker handles outlet/inlet/wall logic.
    """
    blocks: list[str] = []
    for name in region_patches(region.name, config):
        role = patch_role(name, config)
        spec = patch_field_spec(name, "p", config)
        if region.kind == "solid":
            body = _constraint_bc(role) or {
                "type": "calculated", "value": f"uniform {region.p_init:g}",
            }
        else:
            body = _p_bc(role, region, spec)
        blocks.append(_block(name, body))
    for nbr in region.interfaces or ():
        blocks.append(_coupled_wall_block(
            region.name, nbr,
            {"type": "calculated", "value": f"uniform {region.p_init:g}"},
        ))
    return _wrap_field_file(
        region.name, "p", "volScalarField", "[1 -1 -2 0 0 0 0]",
        f"{region.p_init:g}", "".join(blocks),
    )


def build_region_0_p_rgh(region: "RegionSpec", config: dict[str, Any]) -> str:
    """``0/<region>/p_rgh`` BC body.

    Fluid regions: ``fixedFluxPressure`` on walls (preserves momentum-flux
    consistency under buoyancy), ``fixedValue`` on outlets.  Solid regions:
    every patch is ``calculated`` — ``fixedFluxPressure`` is fluid-only
    (it needs the density gradient OpenFOAM only computes inside fluid
    meshes).  Matches the OpenFOAM-4.x ``multiRegionHeaterRadiation``
    ``0/p_rgh`` template distributed to solids.
    """
    blocks: list[str] = []
    for name in region_patches(region.name, config):
        role = patch_role(name, config)
        spec = patch_field_spec(name, "p_rgh", config)
        if region.kind == "solid":
            body = _constraint_bc(role) or {
                "type": "calculated", "value": f"uniform {region.p_init:g}",
            }
        else:
            body = _p_rgh_bc(role, region, spec)
        blocks.append(_block(name, body))
    for nbr in region.interfaces or ():
        coupled_type = (
            "calculated" if region.kind == "solid" else "fixedFluxPressure"
        )
        blocks.append(_coupled_wall_block(
            region.name, nbr,
            {"type": coupled_type, "value": f"uniform {region.p_init:g}"},
        ))
    return _wrap_field_file(
        region.name, "p_rgh", "volScalarField", "[1 -1 -2 0 0 0 0]",
        f"{region.p_init:g}", "".join(blocks),
    )


def build_region_0_T(region: "RegionSpec", config: dict[str, Any]) -> str:
    """``0/<region>/T`` with per-patch BCs + coupled CHT block at every interface.

    Routes the per-patch BC body through either the fluid- or solid-region
    picker depending on ``region.kind``.  The coupled
    ``compressible::turbulentTemperatureCoupledBaffleMixed`` block at each
    interface is the same on both sides up to ``kappaMethod``.
    """
    picker = _T_bc_fluid if region.kind == "fluid" else _T_bc_solid
    blocks: list[str] = []
    for name in region_patches(region.name, config):
        role = patch_role(name, config)
        spec = patch_field_spec(name, "T", config)
        blocks.append(_block(name, picker(role, region, spec)))
    for nbr in region.interfaces or ():
        blocks.append(_coupled_T_block(region, nbr))
    return _wrap_field_file(
        region.name, "T", "volScalarField", "[0 0 0 1 0 0 0]",
        f"{region.T_init:g}", "".join(blocks),
    )



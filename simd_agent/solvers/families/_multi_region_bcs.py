# simd_agent/solvers/families/_multi_region_bcs.py
"""Per-region ``0/<region>/<field>`` renderers for multi-region (CHT) cases.

Why this module exists separately from :mod:`_multi_region`:

  * The 0-field renderers are the ONLY MultiRegionBase responsibility
    that needs to inspect ``config["boundary_conditions"]`` — every other
    deterministic file (thermo, regionProperties, fvSchemes, fvSolution,
    changeDictionaryDict, …) is a function of region topology + presets.
    Putting per-patch BC logic next to topology logic mixes two
    abstraction levels and bloats the base class.
  * Free functions, no ``self`` magic — same style as :mod:`bc_fixers`
    and :mod:`legacy_fixers`.  Each function takes a ``RegionSpec`` plus
    the full ``validated_config`` and returns a complete FOAM dict text.
  * One file per logical concern: patch ownership / role inference /
    block formatting / per-field BC picking / per-field file builders.

Patch model
-----------

After ``splitMeshRegions -cellZones -overwrite``, each region's polyMesh
contains:

  * The mesh patches whose name starts with ``<region>_`` (the user's
    original patches that landed in this region's cellZone).
  * Constraint patches without a region prefix (typically ``front`` /
    ``back`` in 2D cases) — these survive into every region.
  * Auto-generated coupled patches named ``<region>_to_<other>`` (one
    per interface this region shares with a neighbour).

The renderers iterate that union and emit one BC block per patch.  The
universal :func:`bc_fixers.fix_constraint_patch_bcs` runs afterwards in
``validate_full`` and rewrites any block whose mesh-side patch type is
``symmetry`` / ``empty`` / ``wedge`` — we don't try to enforce that here.

BC source authority
-------------------

For each (patch, field) pair we look up, in order:

  1. ``config["boundary_conditions"][patch_name][field]`` — the
     precheck/UI-authored BC spec (``{type, value, …}``).
  2. The patch role inferred from ``patchClass`` / mesh type / name
     suffix (see :func:`patch_role`), mapped to a field-appropriate
     default (``inletOutlet`` for outlets, ``noSlip`` for walls, …).

If neither path produces a value, the field's ``RegionSpec`` init value
is used as the last resort.
"""

from __future__ import annotations

from typing import TYPE_CHECKING, Any

if TYPE_CHECKING:
    from simd_agent.run.case_spec.strategies import RegionSpec



def build_region_0_omega(region: "RegionSpec", config: dict[str, Any]) -> str:
    """``0/<fluid>/omega`` — kOmega / kOmegaSST specific dissipation rate.

    Dimensions ``[0 0 -1 0 0 0 0]`` (s⁻¹).  Walls use ``omegaWallFunction``;
    interior + inlet/outlet use the role-appropriate BC from
    :func:`_omega_bc`.  CHT coupling interfaces use the wall function so
    the boundary layer behaviour on the coupled wall matches what would
    happen on a plain external wall.
    """
    blocks: list[str] = []
    for name in region_patches(region.name, config):
        role = patch_role(name, config)
        spec = patch_field_spec(name, "omega", config)
        blocks.append(_block(name, _omega_bc(role, region, spec)))
    for nbr in region.interfaces or ():
        blocks.append(_coupled_wall_block(
            region.name, nbr,
            {
                "type": "omegaWallFunction",
                "value": f"uniform {region.omega_init:g}",
            },
        ))
    return _wrap_field_file(
        region.name, "omega", "volScalarField", "[0 0 -1 0 0 0 0]",
        f"{region.omega_init:g}", "".join(blocks),
    )


